  // Translation helper function with fallback to English
export function getTranslation(translations, language, key) {
  const keys = key.split('.');
  let value = translations[language];

  for (const k of keys) {
    if (value && typeof value === 'object') {
      value = value[k];
    } else {
      value = undefined;
      break;
    }
  }

  // Fallback to English if translation not found
  if (!value && language !== 'en') {
    let enValue = translations.en;
    for (const k of keys) {
      if (enValue && typeof enValue === 'object') {
        enValue = enValue[k];
      } else {
        enValue = undefined;
        break;
      }
    }
    return enValue || key;
  }

  return value || key;
}